package engine.components.ui;

import engine.components.Component;

public class RectTransform extends Component {
    private int width;
    private int height;

    public RectTransform(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
